// updateElement fonksiyonu g√ºvenli versiyon
function updateElement(id, value) {
    const element = document.getElementById(id);
    if (element) {
        if (element.tagName === 'INPUT') {
            element.value = value || '';
        } else {
            element.textContent = value || '';
        }
    }
}
